-- ============================================================
-- PARTE 1: INTEGER COM IDENTITY
-- ============================================================

-- Opcao A: IDENTITY (Oracle 12c+)
CREATE TABLE cliente_seq (
    cliente_id   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome         VARCHAR2(200) NOT NULL,
    email        VARCHAR2(200),
    data_cadastro DATE DEFAULT SYSDATE
);

-- Testando insercao sem especificar ID
INSERT INTO cliente_seq (nome, email) VALUES ('Maria Silva', 'maria@email.com');
INSERT INTO cliente_seq (nome, email) VALUES ('Joao Souza', 'joao@email.com');
INSERT INTO cliente_seq (nome, email) VALUES ('Ana Costa', 'ana@email.com');

SELECT * FROM cliente_seq;

-- ============================================================
-- PARTE 2: SEQUENCE MANUAL (estilo Oracle tradicional)
-- ============================================================

CREATE SEQUENCE seq_produto START WITH 1000 INCREMENT BY 1;

CREATE TABLE produto_seq (
    produto_id NUMBER DEFAULT seq_produto.NEXTVAL PRIMARY KEY,
    descricao  VARCHAR2(300) NOT NULL,
    preco      NUMBER(10,2)
);

