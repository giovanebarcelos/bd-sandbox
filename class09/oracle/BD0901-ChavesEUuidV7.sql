-- PARTE 5: COMPARACAO DE TAMANHO DE INDICES
-- ============================================================

-- Consultar tamanho dos indices
SELECT index_name, table_name,
       ROUND(bytes / 1024, 2) AS tamanho_kb
  FROM user_segments
 WHERE segment_name IN (
       SELECT index_name FROM user_indexes
        WHERE table_name IN ('CLIENTE_SEQ', 'CLIENTE_UUID_V4', 'CLIENTE_UUID_V7'))
 ORDER BY bytes DESC;
