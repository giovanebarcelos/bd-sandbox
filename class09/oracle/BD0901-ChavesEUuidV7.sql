INSERT INTO produto_seq (descricao, preco) VALUES ('Teclado Mecanico', 350.00);

SELECT * FROM produto_seq;

-- ============================================================
-- PARTE 3: UUID V4 (SYS_GUID)
-- ============================================================

CREATE TABLE cliente_uuid_v4 (
    cliente_id   RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
    nome         VARCHAR2(200) NOT NULL,
    email        VARCHAR2(200),
    data_cadastro DATE DEFAULT SYSDATE
);

INSERT INTO cliente_uuid_v4 (nome, email) VALUES ('Pedro Alves', 'pedro@email.com');
INSERT INTO cliente_uuid_v4 (nome, email) VALUES ('Carla Mendes', 'carla@email.com');

-- Converter RAW(16) para string UUID legivel
SELECT LOWER(REGEXP_REPLACE(RAWTOHEX(cliente_id),
       '(.{8})(.{4})(.{4})(.{4})(.{12})', '\1-\2-\3-\4-\5')) AS uuid_formatado,
       nome, email
  FROM cliente_uuid_v4;

