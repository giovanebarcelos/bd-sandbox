SELECT * FROM cliente_uuid_v7 ORDER BY cliente_id;

-- Extrair timestamp de UUID v7 (aproximado)
SELECT cliente_id,
       ('x' || REPLACE(cliente_id::TEXT, '-', ''))::BIT(48)::BIGINT AS timestamp_approx
  FROM cliente_uuid_v7;

-- ============================================================
-- PARTE 5: COMPARACAO DE TAMANHO DE INDICES
-- ============================================================

SELECT
    relname AS tabela,
    pg_size_pretty(pg_relation_size(relid)) AS tamanho_tabela,
    pg_size_pretty(pg_indexes_size(relid)) AS tamanho_indices
  FROM pg_stat_user_tables
 WHERE relname LIKE 'cliente%'
 ORDER BY relname;

-- Ou via pg_class
SELECT
    c.relname AS tabela,
    pg_size_pretty(pg_table_size(c.oid)) AS tamanho_dados,
    pg_size_pretty(pg_indexes_size(c.oid)) AS tamanho_indices
  FROM pg_class c
 WHERE c.relname LIKE 'cliente%'
   AND c.relkind = 'r'
 ORDER BY c.relname;
