-- ============================================================
-- PARTE 4: UUID V7 (funcao PL/pgSQL ou via aplicacao)
-- ============================================================

-- Funcao uuid_v7() em PL/pgSQL
CREATE OR REPLACE FUNCTION uuid_v7() RETURNS UUID
LANGUAGE plpgsql
AS $$
DECLARE
    v_time_ms  BIGINT;
    v_rand_a   SMALLINT;
    v_rand_b   BIGINT;
BEGIN
    v_time_ms := (EXTRACT(EPOCH FROM clock_timestamp()) * 1000)::BIGINT;
    v_rand_a := (random() * 4096)::INT;  -- 12 bits
    v_rand_b := ((random() * 2^62)::BIGINT) | (10::BIGINT << 62);  -- variant 10xx

    -- Monta UUID: [timestamp 48 bits][version=7 4 bits][rand_a 12 bits]
    --              [variant 2 bits][rand_b 62 bits]
    RETURN encode(
        set_byte(
            set_byte(
                set_byte(
                    set_byte(
                        set_byte(
                            set_byte(
                                set_byte(
                                    set_byte(
                                        '\x00000000000000000000000000000000'::BYTEA,
                                        0, (v_time_ms >> 40)::INT & 255),
                                        1, (v_time_ms >> 32)::INT & 255),
                                        2, (v_time_ms >> 24)::INT & 255),
                                        3, (v_time_ms >> 16)::INT & 255),
                                    4, (v_time_ms >> 8)::INT & 255),
                                5, v_time_ms::INT & 255),
                            6, ((7 << 4) | (v_rand_a >> 8)) & 255),
                        7, v_rand_a & 255),
            'hex'
    )::UUID;
END;
$$;

-- Tabela com UUID v7
CREATE TABLE cliente_uuid_v7 (
    cliente_id   UUID DEFAULT uuid_v7() PRIMARY KEY,
    nome         VARCHAR(200) NOT NULL,
    email        VARCHAR(200),
    data_cadastro DATE DEFAULT CURRENT_DATE
);

