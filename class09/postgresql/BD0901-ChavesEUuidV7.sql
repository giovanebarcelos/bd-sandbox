-- ============================================================
-- PARTE 2: GENERATED AS IDENTITY (padrao SQL, recomendado)
-- ============================================================

-- Opcao A: GENERATED ALWAYS (nao permite override)
CREATE TABLE cliente_identity_always (
    cliente_id   INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    nome         VARCHAR(200) NOT NULL,
    email        VARCHAR(200),
    data_cadastro DATE DEFAULT CURRENT_DATE
);

-- Opcao B: GENERATED BY DEFAULT (permite override em migracoes)
CREATE TABLE cliente_identity_default (
    cliente_id   INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome         VARCHAR(200) NOT NULL,
    email        VARCHAR(200),
    data_cadastro DATE DEFAULT CURRENT_DATE
);

