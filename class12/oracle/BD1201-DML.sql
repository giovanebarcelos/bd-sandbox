-- -----------------------------------------------------------------------------
-- 1. ESQUEMA BOOKHUB
-- -----------------------------------------------------------------------------
CREATE TABLE Autor (
    autor_id        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome            VARCHAR2(150) NOT NULL,
    nacionalidade   VARCHAR2(60)  NOT NULL,
    data_nascimento DATE          NOT NULL
);

CREATE TABLE Livro (
    livro_id        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    autor_id        NUMBER        NOT NULL,
    titulo          VARCHAR2(200) NOT NULL,
    genero          VARCHAR2(60)  NOT NULL,
    preco           NUMBER(10,2)  NOT NULL,
    tipo            CHAR(1)       NOT NULL CHECK (tipo IN ('F','D')), -- F=Fisico, D=Digital
    ano_publicacao  NUMBER(4)     NOT NULL,
    CONSTRAINT autor_livro_fk FOREIGN KEY (autor_id) REFERENCES Autor (autor_id)
);

CREATE TABLE Cliente (
    cliente_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome            VARCHAR2(150) NOT NULL,
    email           VARCHAR2(150) NOT NULL UNIQUE,
    telefone        VARCHAR2(30)  NOT NULL,
    data_cadastro   DATE          NOT NULL
);

CREATE TABLE Venda (
    venda_id        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    data_venda      DATE          NOT NULL,
    forma_pagamento VARCHAR2(30)  NOT NULL,
    valor_total     NUMBER(10,2)  NOT NULL,
    cliente_id      NUMBER        NOT NULL,
    CONSTRAINT cliente_venda_fk FOREIGN KEY (cliente_id) REFERENCES Cliente (cliente_id)
);

CREATE TABLE ItemVenda (
    itemvenda_id    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    venda_id        NUMBER        NOT NULL,
    livro_id        NUMBER        NOT NULL,
    preco_unitario  NUMBER(10,2)  NOT NULL,
    quantidade      NUMBER(6)     NOT NULL,
    CONSTRAINT venda_itemvenda_fk FOREIGN KEY (venda_id) REFERENCES Venda (venda_id),
    CONSTRAINT livro_itemvenda_fk FOREIGN KEY (livro_id) REFERENCES Livro (livro_id)
);

-- Tabela de estoque, usada nos exemplos de MERGE (upsert) mais adiante
CREATE TABLE Estoque (
    livro_id             NUMBER PRIMARY KEY,
    quantidade_disponivel NUMBER(8) NOT NULL,
    ultima_atualizacao    DATE     NOT NULL,
    CONSTRAINT livro_estoque_fk FOREIGN KEY (livro_id) REFERENCES Livro (livro_id)
);

-- Tabela de staging: simula um arquivo/feed do fornecedor com contagens novas de
-- estoque, algumas para livros ja cadastrados em Estoque, outras para livros novos
CREATE TABLE LivroEstoqueStaging (
    livro_id        NUMBER PRIMARY KEY,
    quantidade_nova NUMBER(8) NOT NULL
);

