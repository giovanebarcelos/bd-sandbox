-- -----------------------------------------------------------------------------
-- 3. UPDATE
-- -----------------------------------------------------------------------------
-- 3.1 UPDATE simples: reajuste de 10% em todos os livros fisicos
UPDATE Livro
   SET preco = ROUND(preco * 1.10, 2)
 WHERE tipo = 'F';

-- 3.2 UPDATE com subquery correlacionada: zera estoque de livros descontinuados
--     (ano de publicacao anterior a 2000)
UPDATE Estoque e
   SET quantidade_disponivel = 0
 WHERE EXISTS (SELECT 1 FROM Livro l
                WHERE l.livro_id = e.livro_id
                  AND l.ano_publicacao < 2000);

-- 3.3 UPDATE multi-coluna com CASE: reclassifica forma de pagamento em vendas
UPDATE Venda
   SET forma_pagamento = CASE
                            WHEN forma_pagamento = 'Boleto' THEN 'Boleto Bancario'
                            ELSE forma_pagamento
                          END
 WHERE forma_pagamento = 'Boleto';

COMMIT;

-- -----------------------------------------------------------------------------
-- 4. DELETE
-- -----------------------------------------------------------------------------
-- 4.1 DELETE simples: remove livros anteriores a 1990 (nenhum na carga atual,
--     mas ilustra o padrao antes de popular novos dados de teste)
DELETE FROM Livro WHERE ano_publicacao < 1990;

-- 4.2 DELETE com subquery: remove itens de estoque de livros sem nenhuma venda
--     registrada (demonstra cuidado com integridade referencial)
DELETE FROM Estoque e
 WHERE e.quantidade_disponivel = 0
   AND NOT EXISTS (SELECT 1 FROM ItemVenda iv WHERE iv.livro_id = e.livro_id);

ROLLBACK; -- desfeito propositalmente: mantemos os dados para os proximos passos

-- -----------------------------------------------------------------------------
-- 5. MERGE (upsert nativo do Oracle)
-- -----------------------------------------------------------------------------
-- Cenario: o fornecedor envia uma atualizacao de estoque (LivroEstoqueStaging).
-- Se o livro ja existe em Estoque, ATUALIZA a quantidade; senao, INSERE a linha.
INSERT INTO LivroEstoqueStaging (livro_id, quantidade_nova) VALUES (1, 8);   -- existe -> update
INSERT INTO LivroEstoqueStaging (livro_id, quantidade_nova) VALUES (3, 20);  -- existe -> update
INSERT INTO LivroEstoqueStaging (livro_id, quantidade_nova) VALUES (6, 15);  -- novo   -> insert
INSERT INTO LivroEstoqueStaging (livro_id, quantidade_nova) VALUES (7, 40);  -- novo   -> insert
COMMIT;

MERGE INTO Estoque tgt
USING LivroEstoqueStaging src
   ON (tgt.livro_id = src.livro_id)
WHEN MATCHED THEN
     UPDATE SET tgt.quantidade_disponivel = src.quantidade_nova,
                tgt.ultima_atualizacao    = SYSDATE
WHEN NOT MATCHED THEN
     INSERT (livro_id, quantidade_disponivel, ultima_atualizacao)
     VALUES (src.livro_id, src.quantidade_nova, SYSDATE);

COMMIT;

-- Conferindo o resultado do MERGE
SELECT livro_id, quantidade_disponivel, ultima_atualizacao FROM Estoque ORDER BY livro_id;

-- -----------------------------------------------------------------------------
-- 6. MERGE com condicao adicional de DELETE (recurso avancado do Oracle)
-- -----------------------------------------------------------------------------
-- Remove do estoque qualquer linha cuja quantidade recem-sincronizada seja zero
MERGE INTO Estoque tgt
USING (SELECT 5 AS livro_id, 0 AS quantidade_nova FROM dual) src
   ON (tgt.livro_id = src.livro_id)
WHEN MATCHED THEN
     UPDATE SET tgt.quantidade_disponivel = src.quantidade_nova
     DELETE WHERE tgt.quantidade_disponivel = 0
WHEN NOT MATCHED THEN
     INSERT (livro_id, quantidade_disponivel, ultima_atualizacao)
     VALUES (src.livro_id, src.quantidade_nova, SYSDATE);

COMMIT;

